var filtersObject = {};

//on filter change
$(".filter").on("change",function() {
	var filterName = $(this).data("filter"),
		filterVal = $(this).val();
	
	if (filterVal == "") {
		delete filtersObject[filterName];
	} else {
		filtersObject[filterName] = filterVal;
	}
	
	var filters = "";
	
	for (var key in filtersObject) {
	  	if (filtersObject.hasOwnProperty(key)) {
			filters += "[data-"+key+"='"+filtersObject[key]+"']";
	 	}
	}
	
	if (filters == "") {
		$(".product").show();
	} else {
		$(".product").hide();
		$(".product").hide().filter(filters).show();
	}
});

//on search form submit
$("#search-form").submit(function(e) {
	e.preventDefault();
	var query = $("#search-form input").val().toLowerCase();

	$(".product").hide();
	$(".product").each(function() {
		var adı = $(this).data("adı").toLowerCase(),
			hücre = $(this).data("hücre").toLowerCase(),
			seviye = $(this).data("seviye").toLowerCase(),
			bonus = $(this).data("bonus").toLowerCase(),
			güç = $(this).data("güç").toLowerCase();

		if (adı.indexOf(query) > -1 || hücre.indexOf(query) > -1 || seviye.indexOf(query) > -1 || bonus.indexOf(query) > -1 || güç.indexOf(query) > -1) {
			$(this).show();
		}
	});
});